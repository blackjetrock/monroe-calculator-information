////////////////////////////////////////////////////////////////////////////////

uint8_t screen[8192];

int cursor_on = 1;
int cursor_counter = 0;

void update_screen(void)
{
  // Cursor Position (Row, Col): ESC [ <row>;<col> H (e.g., 10,10)
  // Save Cursor Position: ESC 7 (DECSC)
  // Restore Cursor Position: ESC 8 (DECRC)

  cursor_counter++;

  if( cursor_counter > 12 )
    {
      cursor_on = !cursor_on;
      cursor_counter = 0;
    }
  
  // Compare screen buffer with memory, update any changes
  int x = 0;
  int y = 0;

  int vdu_start = VDU_START_ADDRESS;
  int vdu_addr = (vdu_start & 0x07FF) | 0x1000;
  int cur_addr = (VDU_CURSR_ADDRESS & 0x07FF) | 0x1000;
  
  printf("%c7", 27);
  printf("%c[?25l", 27);
  
  for(int i=0; i< 2000; i++)
    {
      if( mem_data[vdu_addr] != screen[i],1 )
        {

          printf("%c[%d;%dH", 27, y+2, x+2);
          if( cur_addr == vdu_addr )
            {
              if( cursor_on )
                {
                  printf("%c", '_');
                }
              else
                {
                  printf("%c", mem_data[vdu_addr]);
                }
            }
          else
            {
              printf("%c", mem_data[vdu_addr]);
            }
          
          //screen[i] = mem_data[vdu_addr];
        }

      vdu_addr = ((vdu_addr+1) & 0x07ff) | 0X1000;

      x++;
      
      if( x >= 80 )
        {
          x = 0;
          y++;
        }
    }
  
  printf("%c8", 27);
  printf("%c[?25h", 27);
}

//------------------------------------------------------------------------------

void cli_vdu_loop(void)
{
  int done = 0;
  int  key;
  
  printf("\nVDU Loop...\n");

  for(int i=0; i<200; i++)
    {
      printf("\n");
    }
  
  // Inital screen display
  
  cli_get_vdu();
  
  while(!done)
    {
      update_screen();
      
      if( ((key = getchar_timeout_us(1000)) != PICO_ERROR_TIMEOUT))
        {
          switch(key)
            {
            case '_':
              cli_get_vdu();
              break;
              
            case 27:
              done = 1;            
              printf("\nExit from VDU Loop\n");
              break;

            default:
              // Send to Acorn
              mem_data[ACORN_PORTD] = key | 0x80;
              sleep_us(100);
              mem_data[ACORN_PORTD] = key;
              break;
            }
	}
    }
}

